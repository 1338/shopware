<?php declare(strict_types=1);

namespace Shopware\Api\Config\Repository;

use Shopware\Api\Config\Collection\ConfigFormFieldValueBasicCollection;
use Shopware\Api\Config\Collection\ConfigFormFieldValueDetailCollection;
use Shopware\Api\Config\Definition\ConfigFormFieldValueDefinition;
use Shopware\Api\Config\Event\ConfigFormFieldValue\ConfigFormFieldValueAggregationResultLoadedEvent;
use Shopware\Api\Config\Event\ConfigFormFieldValue\ConfigFormFieldValueBasicLoadedEvent;
use Shopware\Api\Config\Event\ConfigFormFieldValue\ConfigFormFieldValueDetailLoadedEvent;
use Shopware\Api\Config\Event\ConfigFormFieldValue\ConfigFormFieldValueIdSearchResultLoadedEvent;
use Shopware\Api\Config\Event\ConfigFormFieldValue\ConfigFormFieldValueSearchResultLoadedEvent;
use Shopware\Api\Config\Struct\ConfigFormFieldValueSearchResult;
use Shopware\Api\Entity\Read\EntityReaderInterface;
use Shopware\Api\Entity\RepositoryInterface;
use Shopware\Api\Entity\Search\AggregationResult;
use Shopware\Api\Entity\Search\Criteria;
use Shopware\Api\Entity\Search\EntityAggregatorInterface;
use Shopware\Api\Entity\Search\EntitySearcherInterface;
use Shopware\Api\Entity\Search\IdSearchResult;
use Shopware\Api\Entity\Write\EntityWriterInterface;
use Shopware\Api\Entity\Write\GenericWrittenEvent;
use Shopware\Api\Entity\Write\WriteContext;
use Shopware\Context\Struct\ShopContext;
use Symfony\Component\EventDispatcher\EventDispatcherInterface;

class ConfigFormFieldValueRepository implements RepositoryInterface
{
    /**
     * @var EntityReaderInterface
     */
    private $reader;

    /**
     * @var EntityWriterInterface
     */
    private $writer;

    /**
     * @var EntitySearcherInterface
     */
    private $searcher;

    /**
     * @var EntityAggregatorInterface
     */
    private $aggregator;

    /**
     * @var EventDispatcherInterface
     */
    private $eventDispatcher;

    public function __construct(
        EntityReaderInterface $reader,
        EntityWriterInterface $writer,
        EntitySearcherInterface $searcher,
        EntityAggregatorInterface $aggregator,
        EventDispatcherInterface $eventDispatcher
    ) {
        $this->reader = $reader;
        $this->writer = $writer;
        $this->searcher = $searcher;
        $this->aggregator = $aggregator;
        $this->eventDispatcher = $eventDispatcher;
    }

    public function search(Criteria $criteria, ShopContext $context): ConfigFormFieldValueSearchResult
    {
        $ids = $this->searchIds($criteria, $context);

        $entities = $this->readBasic($ids->getIds(), $context);

        $aggregations = null;
        if ($criteria->getAggregations()) {
            $aggregations = $this->aggregate($criteria, $context);
        }

        $result = ConfigFormFieldValueSearchResult::createFromResults($ids, $entities, $aggregations);

        $event = new ConfigFormFieldValueSearchResultLoadedEvent($result);
        $this->eventDispatcher->dispatch($event->getName(), $event);

        return $result;
    }

    public function aggregate(Criteria $criteria, ShopContext $context): AggregationResult
    {
        $result = $this->aggregator->aggregate(ConfigFormFieldValueDefinition::class, $criteria, $context);

        $event = new ConfigFormFieldValueAggregationResultLoadedEvent($result);
        $this->eventDispatcher->dispatch($event->getName(), $event);

        return $result;
    }

    public function searchIds(Criteria $criteria, ShopContext $context): IdSearchResult
    {
        $result = $this->searcher->search(ConfigFormFieldValueDefinition::class, $criteria, $context);

        $event = new ConfigFormFieldValueIdSearchResultLoadedEvent($result);
        $this->eventDispatcher->dispatch($event->getName(), $event);

        return $result;
    }

    public function readBasic(array $ids, ShopContext $context): ConfigFormFieldValueBasicCollection
    {
        /** @var ConfigFormFieldValueBasicCollection $entities */
        $entities = $this->reader->readBasic(ConfigFormFieldValueDefinition::class, $ids, $context);

        $event = new ConfigFormFieldValueBasicLoadedEvent($entities, $context);
        $this->eventDispatcher->dispatch($event->getName(), $event);

        return $entities;
    }

    public function readDetail(array $ids, ShopContext $context): ConfigFormFieldValueDetailCollection
    {
        /** @var ConfigFormFieldValueDetailCollection $entities */
        $entities = $this->reader->readDetail(ConfigFormFieldValueDefinition::class, $ids, $context);

        $event = new ConfigFormFieldValueDetailLoadedEvent($entities, $context);
        $this->eventDispatcher->dispatch($event->getName(), $event);

        return $entities;
    }

    public function update(array $data, ShopContext $context): GenericWrittenEvent
    {
        $affected = $this->writer->update(ConfigFormFieldValueDefinition::class, $data, WriteContext::createFromShopContext($context));
        $event = GenericWrittenEvent::createWithWrittenEvents($affected, $context, []);
        $this->eventDispatcher->dispatch(GenericWrittenEvent::NAME, $event);

        return $event;
    }

    public function upsert(array $data, ShopContext $context): GenericWrittenEvent
    {
        $affected = $this->writer->upsert(ConfigFormFieldValueDefinition::class, $data, WriteContext::createFromShopContext($context));
        $event = GenericWrittenEvent::createWithWrittenEvents($affected, $context, []);
        $this->eventDispatcher->dispatch(GenericWrittenEvent::NAME, $event);

        return $event;
    }

    public function create(array $data, ShopContext $context): GenericWrittenEvent
    {
        $affected = $this->writer->insert(ConfigFormFieldValueDefinition::class, $data, WriteContext::createFromShopContext($context));
        $event = GenericWrittenEvent::createWithWrittenEvents($affected, $context, []);
        $this->eventDispatcher->dispatch(GenericWrittenEvent::NAME, $event);

        return $event;
    }

    public function delete(array $ids, ShopContext $context): GenericWrittenEvent
    {
        $affected = $this->writer->delete(ConfigFormFieldValueDefinition::class, $ids, WriteContext::createFromShopContext($context));
        $event = GenericWrittenEvent::createWithDeletedEvents($affected, $context, []);
        $this->eventDispatcher->dispatch(GenericWrittenEvent::NAME, $event);

        return $event;
    }
}
